    log "‚ö†Ô∏è –ë–∞–∑–∞ –¥–∞–Ω–Ω—ã—Ö –Ω–µ –Ω–∞–π–¥–µ–Ω–∞"
fi

# –†–µ–∑–µ—Ä–≤–Ω–æ–µ –∫–æ–ø–∏—Ä–æ–≤–∞–Ω–∏–µ –ª–æ–≥–æ–≤
if [ -d "logs" ]; then
    tar -czf $BACKUP_DIR/logs_${TIMESTAMP}.tar.gz logs/
    log "‚úÖ –õ–æ–≥–∏ –∞—Ä—Ö–∏–≤–∏—Ä–æ–≤–∞–Ω—ã: logs_${TIMESTAMP}.tar.gz"
